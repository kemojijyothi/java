//all artimetic operators using char and number
class Demo
{
 public static void main(String [] args)
 {
    int num1=20;
    char char1='a';
    System.out.println(num1+char1);
    System.out.println(num1 + " adding number and char "+ char1+" = " +(num1+char1));
    System.out.println(num1 + " subtracting number and char "+ char1+" = " +(num1-char1));
    System.out.println(num1 + " multipication number and char "+ char1+" = " +(num1*char1));
    System.out.println(num1 + " division of number and char "+ char1+" = " +(num1/char1));
    System.out.println(num1 + " module of number and char "+ char1+" = " +(num1%char1));
    
    
 }
}
