//write a program to print remainder without using divison and module operators

class CountOddNum
{   public static void main(String[] args)
    {
       int count=0;
       for(int i=11;i<=45;i++)
       {
        
        if(i%2!=0){
            count++;
        }
       
        }
        System.out.println(count);
   }
       
    
    
}
