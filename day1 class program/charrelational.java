// using two char data types
class RelationalOperators2
{
 public static void main(String [] main)
 {
    char char1='a';
    char char2='c';
    System.out.println(char1+ " is greater than " +char2+"="+ (char1 < char2));
    System.out.println(char1+ " is greater than " +char2+"="+ (char1 <= char2));
    System.out.println(char1+ " is greater than " +char2+"="+ (char1 >= char2));
 }
}