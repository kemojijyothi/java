//perfect square
import java.util.Scanner;
class PerfectSquarecount{
    public static void main(String []args){
        
       
        for(int i=1;i<=500;i++){
            
          for(int j=1;j<=i;j++){
            if(j*j==i){
                System.out.println(i);
                
            }
        }

        }
        
        
    }
}